package org.herodotus.domain;

public class PageInfo {
	
	private long id;

	
	/**
	 * @param id
	 */
	public PageInfo(long id) {
		super();
		this.id = id;
	}

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	
	

}
